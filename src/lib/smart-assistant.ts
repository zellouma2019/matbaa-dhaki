/// البحث عن أفضل إجابة لسؤال المستخدم
export function findAnswer(query: string): { answer: string; matched: FAQItem | null; suggestions: FAQItem[] } {
  const q = query.toLowerCase().trim();
  if (!q) return { answer: "", matched: null, suggestions: FAQS.slice(0, 4) };

  let bestMatch: FAQItem | null = null;
  let bestScore = 0;

  for (const faq of FAQS) {
    let score = 0;
    // مطابقة الكلمات المفتاحية
    for (const kw of faq.keywords) {
      if (q.includes(kw.toLowerCase())) {
        score += kw.length > 3 ? 3 : 2;
      }
    }
    // مطابقة كلمات السؤال
    const qWords = faq.question.toLowerCase().split(/\s+/);
    for (const w of qWords) {
      if (w.length > 2 && q.includes(w)) {
        score += 1;
      }
    }
    // مطابقة الإجابة نفسها
    const aWords = faq.answer.toLowerCase();
    for (const w of q.split(/\s+/)) {
      if (w.length > 3 && aWords.includes(w)) {
        score += 1;
      }
    }
    if (score > bestScore) {
      bestScore = score;
      bestMatch = faq;
    }
  }

  if (bestMatch && bestScore >= 2) {
    // اقتراحات أخرى من نفس التصنيف
    const suggestions = FAQS.filter(
      (f) => f.id !== bestMatch!.id && f.category === bestMatch!.category,
    ).slice(0, 3);
    return { answer: bestMatch.answer, matched: bestMatch, suggestions };
  }

  // لم يوجد تطابق جيد
  return {
    answer: "لم أجد إجابة دقيقة لسؤالك. إليك أكثر الأسئلة شيوعاً:",
    matched: null,
    suggestions: FAQS.slice(0, 4),
  };
}
